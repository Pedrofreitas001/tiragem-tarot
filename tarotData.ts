// Helper function to get card by ID
export const getCardById = (id: string): TarotCardData | undefined => {
  return TAROT_CARDS.find(card => card.id === id);
};

// Helper function to get cards by arcana
export const getCardsByArcana = (arcana: 'major' | 'minor'): TarotCardData[] => {
  return TAROT_CARDS.filter(card => card.arcana === arcana);
};

// Helper function to get cards by suit
export const getCardsBySuit = (suit: string): TarotCardData[] => {
  return TAROT_CARDS.filter(card => card.suit === suit);
};

// Helper function to get random cards
export const getRandomCards = (count: number): TarotCardData[] => {
  const shuffled = [...TAROT_CARDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Helper function to get card name by ID with language support
export const getCardName = (cardIdOrName: string, isPortuguese: boolean = true): string => {
  const card = TAROT_CARDS.find(c => c.id === cardIdOrName || c.name === cardIdOrName);
  return card ? (isPortuguese ? card.name_pt : card.name) : cardIdOrName;
};

// Helper function to get card by slug (for SEO-friendly URLs)
export const getCardBySlug = (slug: string): TarotCardData | undefined => {
  return TAROT_CARDS.find(c => c.slug === slug || c.slug_pt === slug);
};

export default TAROT_CARDS;
